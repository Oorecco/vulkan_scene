// Build the R8 atlas from the bitmap above
inline void buildFontAtlas(uint8_t* out, int w, int h) {
    for (int g = 0; g < GLYPH_CNT; g++) {
        int col = g % ATLAS_COLS;
        int row = g / ATLAS_COLS;
        for (int py = 0; py < 8; py++)
            for (int px = 0; px < 8; px++)
                out[(row*8+py)*w + (col*8+px)] =
                    ((FONT8[g][py] >> px) & 1) ? 255 : 0;
    }
}
